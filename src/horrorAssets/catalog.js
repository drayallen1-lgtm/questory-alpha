/** Bundled offline horror media — served from /public/media/horror */

export const HORROR_MEDIA_BASE = '/media/horror';

export function horrorPath(segment) {
  return `${HORROR_MEDIA_BASE}${segment}`;
}

const img = (file) => horrorPath(`/images/${file}`);
const aud = (file) => horrorPath(`/audio/${file}`);

