export function resolveHorrorAssetUrl(asset) {
  if (!asset) return '';
  return (
    asset.assetUrl ||
    asset.audioUrl ||
    asset.previewUrl ||
    asset.publicUrl ||
    ''
  );
}

export function libraryAssetForInsert(asset) {
  if (!asset) return asset;
  const base = {
    ...asset,
    publicUrl: resolveHorrorAssetUrl(asset),
    assetUrl: asset.assetUrl || asset.previewUrl || '',
    audioUrl: asset.audioUrl || '',
    previewUrl: asset.previewUrl || asset.assetUrl || asset.icon,
    offline: asset.offline !== false,
    source: asset.source || 'library',
  };
  return base;
}
